function togglePasswordVisibility() {
    var passwordInput = document.getElementById("floatingPassword");
    var checkbox = document.getElementById("exampleCheck1");
    if (checkbox.checked) {
        passwordInput.type = "text";
    } else {
        passwordInput.type = "password";
    }
}
